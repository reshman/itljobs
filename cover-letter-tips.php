            <h1>Cover Letter Writing Tips</h1>
        </div>
        <p>
            When you need to write a cover letter, it's sometimes the small things that make a big difference. Review these tips and techniques for writing top notch cover letters to send with your resume, including format and presentation, choosing a type of cover letter, writing custom cover letters, how to send, and examples and templates.
        </p>

        <p>There are several types of cover letters that can be sent to employers and contacts. Be sure to choose a type of cover letter that reflects how you are applying for the job or the type of job search assistance you are requesting. Your cover letter should be designed specifically for the purpose you are writing and customized for each position you seek.</p>

        <p>When applying for a job a cover letter should be sent or posted with your resume or curriculum vitae. This collection of free professionally written cover letter examples will help you get started.</p>

        <p>Below you'll find both hard copy and email examples, for a variety of different types of employment inquiries and job applications including general cover letters, cold contact cover letters, referral letters, customized cover letters, job promotion letters, networking outreach letters, and letters to inquire about unadvertised openings.</p>

        <p> <strong> Cover Letter Content </strong><br>
            First of all, your cover letter needs to include your contact information (name, address, phone, email) so prospective employers can get in touch with you.</p>
        <p>Your cover letter should include at least three paragraphs:</p>

        <p> <ol><li>What you are applying for and where you found the job posting - First Paragraph</li>
            <li>What you have to offer - Middle Paragraph(s)</li>
            <li>How you will follow-up - Final Paragraph</li>
            </ol>
        </p>

        <p>In the first paragraph, if you are writing in response to a job posting, indicate where you learned of the position and the title of the position. More importantly, express your enthusiasm and the likely match between your credentials and the position's qualifications.</p>

        <p>The second paragraph of your cover letter should refer specifically to the qualifications listed in the job posting and illustrate how your particular abilities and experiences relate to the position for which you are applying.</p>

        <p>The final paragraph of your letter should reiterate your interest in the job and let the employer know how they can reach you and include your phone number and email address.</p>

        <p><strong>Cover Letter Format</strong><br>
            You know that “goals and objectives” statement at the top of your resume? Skip it, say experts. Your goal is obvious, after all – you want a job. Instead, use a summary statement that focuses on your past accomplishments. Show hiring managers and interviewers what you’ve done in the past and how that will make you a better employee for them.</p>

        <p><strong>3. Show employers what you’ve done</strong><br>
            Cover letter presentation matters as much as what you include. When writing cover letters it's important to use a basic font that is easy to read. Depending on the hiring process your cover letter may be viewed in an applicant tracking system or other online hiring system. Those systems work best reading simple text rather than fancy formatting.</p>
           <p>It's also important for the hiring manager to be able to easily read your resume. Using a basic 12 point font will ensure that your cover letter is easy to read. Basic fonts like Arial, Verdana, and Times New Roman work well. Don't forget to leave space between paragraphs and to proof your letter before you send or upload it. Do remember that your cover letter fonts should match your resume.</p>


        <p><strong>Cover Letter Keywords</strong><br>
            When you are writing a cover letter, it's important to make sure that every word counts. Your cover letters should enhance the employer's appreciation of your qualifications so you can move from applicant to interviewee.</p>

        <p>Keywords are a vital element of a persuasive cover letter which portrays the candidate as a highly qualified applicant for the job. These words fall into three general categories: skill words, results oriented words, and words which show recognition for achievements.</p>

        <p>Keywords work in a couple of different ways. First, the keywords you include in your resume and cover letter will be used to match your application with the skills required by the employer. Secondly, keywords that are incorporated into a cover letter will show the hiring manager how and why you are highly qualified for the job.</p>

        <p><strong>Skill Keywords</strong><br>
            Job seekers should carefully analyze the skills required to excel in their target job and incorporate them into their cover letter.</p>

        <p>Those keywords should also be included in your resume. It will be more genuine if you paraphrase the skills mentioned in job ads as opposed to listing them verbatim. Skill words are most effective when connected to a specific role or project where the skills were crucial to success.</p>

        <p><strong>Results Oriented Keywords</strong><br>
            All employers are looking for employees who will add value and generate positive results for their organization. That's why it's critical to integrate results oriented language into your cover letters. Think about the bottom line for each job on your resume and how you might have made things better in your role.
        </p>

        <p>Your cover letter should showcase your accomplishments, not just your skills or personal qualities. Results oriented words are most effective when coupled with some numbers which quantify your impact as in "I reduced turnover amongst first year hires by 20% by implementing a mentoring system." By using these types of keywords you are clearing showing what you accomplished in your previous roles.</p>

        <p><strong>Recognition Keywords</strong><br>
            Hiring managers will be more likely to believe that you will be an outstanding performer if it is clear that previous employers have viewed you in this way. One way to do this is to incorporate language which demonstrates that employers have recognized your contributions.
        </p>

        <p>Ideally, recognition phrases will include the type of individual who noted your achievement and the basis for your recognition. For example, one might say "I was designated as the team leader for the budget reduction taskforce by my Division Vice President based on my previous record of accruing cost savings." Recognition keywords attest to how you have excelled in your previous jobs and how you have accomplished more than was required.</p>

        <p><strong>How To Write a Customized Cover Letter</strong><br>
            It can be time-consuming to write a custom cover letter for each job you apply for, but it's important to take the time and effort to show the company why you are a good match.  Here's how to customize your cover letter when applying for a job.
        </p>

        <p><strong>How To Write a Customized Cover Letter</strong><br>
            <strong>Look for the Hiring Manager</strong><br>
            <p>When it comes to cover letters, taking the time to get personal is really important. Find out as much as you can about the company and the hiring manager. Personalize your cover letter and, if you can, address it to the individual responsible for hiring. If need be, research online or make a phone call to find out who the hiring manager is.</p>

        </p>

        <p><strong>Mention Who You Know</strong><br>
            If you know someone at the company, mention their name in your cover letter. Name dropping works - your cover letter will get a closer look if it mentions someone who works at the company. That's important both from your perspective and from the employee's, especially if the company has an Employee Referral Program and is eligible for a bonus.
        </p>

        <p><strongMention How You Learned About the Job</strong><br>
            Mention how you learned about the job in the first paragraph of your cover letter. The company wants to know how the job was sourced, especially when you found the listing on a job board or other site where they paid to post. That sentence can simply say, for example, "I learned of this position from the posting I read on Monster."
        </p>

        <p>Take it one step further and mention something about the company, from the mission statement on the company website, for example, in your cover letter.</p>

        <p><strong>Showcase Your Qualifications</strong><br>
            Employers typically won't even consider a candidate who doesn't appear to be qualified at first glance. That first glance at your cover letter is your one opportunity to make a good impression and make it to the next round. In order to pass that first round of screening, you must specifically address the job ad and state why you are qualified for the position
        </p>

        <p>To write a custom cover letter, take the job posting and list the criteria the employer is looking for. Then list the skills and experience you have. Either address how your skills match the job in paragraph form or list the criteria and your qualifications.Here's more on how to match your qualifications to a job description.</p>

        <p><strong>Mention Include Skills and Results</strong><br>
            Also include skill, results and recognition keywords in your cover letter to increase your chances of getting selected for an interview. Here's a list of skills for resumes and cover letters you can use to get suggestions for your own application materials.
        </p>

        <p><strong>How to Address a Cover Letter</strong><br>
            Review these options for addressing cover letters when you're not sure to whom to address them.
        </p>

        <p>If you don’t have a contact person at the company, you can either leave off the salutation from your cover letter and start with the first paragraph of your letter or use a general salutation.</p>

        <p><strong>When You Use a General Salutation</strong><br>
            There a variety of cover letter salutations you can use to address your letter. In a survey of more than 2,000 companies, Saddleback College found that employers preferred the following greetings:
        </p>

        <p>
            <ul>
                <li>Dear Hiring Manager (40%)</li>
                <li>To Whom It May Concern (27%)</li>
                <li>Dear Sir/Madam (17%)</li>
                <li>Dear Human Resources Director (6%)</li>
                <li>Leave it blank (8%)</li>
            </ul>
        </p>

        <p>Once you have chosen a salutation, follow it with a colon or comma, a space, and then start the first paragraph of your letter. For example:</p>
        <p>Dear Hiring Manager:</p>
        <p>First paragraph of letter.</p>


        <p><strong>How to Address a Cover Letter for a Non-Gender Specific Name</strong><br>
            If you do have a name, but aren't sure of the person's gender, an option is to include both the first name and the last name in your salutation:
        </p>
        <p>
            <ul>
                <li>Dear Sydney Doe</li>
                <li>Dear Taylor Smith</li>
            </ul>
        With these types of gender-ambiguous names, LinkedIn can be a helpful resource. Since many people include a photo with their profile, a simple search of the person's name and company within LinkedIn could potentially turn up the contact's photograph.
        </p>

        <p><strong>How to Email Cover Letter and Resume Attachments</strong><br>
            When you apply for jobs via email, you may need to send your resume and cover letter as an attachment. It's important to send your cover letter and resume attachments correctly, to include all the information you need so your email message is read, and to let the receiver know how they can contact you to schedule an interview.
        </p>

        <p>When you are sending cover letter and resume attachments, the first step is to save your resume as a PDF or a Word document. This way the receiver will get a copy of the resume and letter in the original format.</p>
        <p>If you have word processing software other than Microsoft Word save your resume as a Word (.doc or .docx) document. File, Save As, should be an option in your program.</p>
        <p>Depending on your word processing software, you may be able to File, Print to Adobe PDF, to save your documents as a PDF.</p>
        <p>If not, there are programs you can use to convert a file to a PDF.</p>
        <p>Use your name as the file name, so the employer knows whose resume and cover letter it is i.e. janedoeresume.doc and janedoecoverletter.doc.</p>


        <p><strong>How to Send an Email Message with a Cover Letter and Resume</strong><br>
            Once you have saved your resume and cover letter and they are ready to send, the next step is write an email message to send with your documents.
        </p>

        <p>This example of sending a cover letter attachment is shown using Microsoft Outlook. First, open your email account. Then click on Message at the top left of the screen or click on File, New, Message.</p>
        <p>You can either type your cover letter directly into the email message, copy and paste from a word processing document, or, if the company requests an attachment, send your cover letter with the email message.</p>
        <p>Make sure to follow the directions in the job posting when sending your cover letter and resume or your application may not be considered.</p>


        <p><strong>Add a Signature to an Email Message</strong><br>
            It is important to include an email signature with all your contact information, so it's easy for hiring managers and recruiters to get in touch with you. Include your full name, your email address, and your phone number in your email signature, so the hiring manager can see, at a glance, how to contact you.
        </p>

        <p>To add your signature to your email message, click on File, Insert, Signature if you have a signature saved that you use for job searching.</p>
        <p>If you haven't created an email signature, type your contact information (name, email address, phone) at the bottom of your message.</p>

        <p><strong>How to Attach a Resume and Cover Letter to an Email Message</strong><br>
            Once your email message is ready to send, you need to attach your resume and cover letter to your message. Click on Insert, Attach File. Microsoft Outlook will display a list of files in the default file folder of your computer.
        </p>

        <p>If your resume and cover letter are stored in a different folder, click on the appropriate folder.</p>
        <p>Click to select the file you want to add to your email message, and then click on Insert to attach the document to your email message.</p>
        <p>Before you click Send, send the message to yourself to be sure all the attachments come through and your email message is perfect.</p>
        <p>Send a copy of the message to yourself, as well as to the company, so you have a copy for your records. Add yourself as a Bcc (blind carbon copy) by clicking Bcc... and adding your email address.</p>






