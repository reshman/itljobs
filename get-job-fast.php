  <p><strong>4. Volunteer</strong><br>
Dedicating some of your time to helping others could make it much easier to find a job, according to a 2015 study published in the Journal of Career Assessment. The study looked at 265 unemployed twenty something and found that those who volunteered were more likely to have a job after six months than those who didn’t. Even volunteering for just an hour a week was associated with a greater chance of finding a job.</p> 

<p>One reason that volunteering may so useful for young people looking for a job is that it demonstrates to employers that they have specific job-related skills – skills they may not be developing in college</p>

  <p><strong>5. Clean up your social media</strong><br>
You should really know this by now, but here’s a reminder just in case you forgot: There’s a very good chance your potential employer is checking out your social media profiles. Forty-three percent of employers are researching candidates on social media, according to a CareerBuilder survey. If they don’t like what they see, it could cost you the job.</p>

<p>What social media behaviours turn off employers? Nearly half said they’d pass on an applicant who posted “provocative or inappropriate photographs or information.” Other social media no-no’s included:</p>
