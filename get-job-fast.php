 <p> <strong> 1. Time your search right </strong><br>
            Some seasons are more favourable for job searchers than others. Obviously, if you need work right away, you don’t have the luxury of waiting to look for a job. But if you’re itching for a change, you might want to start your search at a time of year when companies are more likely to be looking for new employees.</p>
            <p>The big months for hiring are January and February, and late September and October. Job seekers who make contact right at the start of these cycles have the best chance of being hired.</p>
            <p>Hiring often slows down in the summer, since many people are out of the office. If you’re thinking about looking for a new job now, consider spending the next month or two fine-tuning your resume, updating your LinkedIn profile, and building your network. Then, you can hit the ground running when hiring kicks into high gear around Labour Day.</p>

  <p><strong>2.  Show employers what you’ve done</strong><br>
You know that “goals and objectives” statement at the top of your resume? Skip it, say experts. Your goal is obvious, after all – you want a job. Instead, use a summary statement that focuses on your past accomplishments. Show hiring managers and interviewers what you’ve done in the past and how that will make you a better employee for them.</p>

