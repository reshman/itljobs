            <h1>HOW TO PREPARE A RESUME</h1>
        </div>


        <p> <strong> 1. Formatting Your Resume</strong><br>
            The first thing that a possible employer will see on your resume is the text. For that reason, it is very important that you make the right first impression. Choose a professional font in a size 11 or 12. Times New Roman is the classic serif font, while Arial and Calibri are two of the better choices for sans-serif. Even though Sans Serif fonts are more popular for resume, Yahoo has chosen Helvetica as the best font to use for your resume.</p>
        <p>
            <ul>
                <li>Times New Roman is actually a little hard to read on a screen for many people. If you are emailing your resume, consider using Georgia instead for a more readable serif font.</li>
                <li>You can use multiple fonts for different parts of your resume, but try to limit it to two maximum. Instead of changing between fonts, try making specific sections bold or italicized instead.</li>
                <li>The font for your header and the introduction to a section may be a size 14 or 16, but otherwise you should not use a very large font.</li>
                <li>Your text should always be printed in solid black ink. Make sure to deactivate any hyperlinks (like to your email) so that they don’t print in blue or another contrasting color.</li>
            </ul>
        </p>

        <p> <strong> Set up the page</strong><br>
            Your page should have one inch margins all the way around with 1.5 or 2 point line spacing. The body of your resume will be aligned to the left and your header should be centered at the top of your page.</p>
        <p>

        <p> <strong> Create your heading</strong><br>
            This is the section at the top of your resume which gives all of your contact information including your name, address, email, and phone number. Your name should be in a slightly larger size - either 14 or 16 point font. If you have both, list your home and cell phone numbers.</p>
        <p>

        <p> <strong> Decide on a layout</strong><br>
            There are three general formats for creating a resume: chronological, functional, or combination. Your work history and the type of job you are applying for will determine the layout style you should use.</p>
        <p>

        <p>
            <ul>
                <li>
                    <b>Chronological resumes</b> are used for showing a steady growth in a particular career field. These are best used for someone applying to a job within their career-path to show an increase of responsibility over time.
                </li>
                <li>
                    <b>Functional resumes</b> are focused on skills and experience rather than job history. These are best used for someone who may have holes in their work history or who have gained experience from being self-employed for a time.
                </li>

                <li>
                    <b>Combination resumes</b> are, as it sounds, a combination of both a chronological and a functional resume. These are used to show off specific skills and how they were acquired. If you have developed a specific skill set from working in a variety of related fields, then this is the best resume option for you.
                </li>
            </ul>
        </p>

        <p> <strong> Chronological resumes</strong><br>
            <b>List your employment history.</b> As this is a chronological resume, your jobs should be listed in chronological order with your most recent employment first. Include the name of the company, its location, your title, your duties and responsibilities while working there, and the dates that you were employed there.</p>
        <p>

        <p>
            <ul>
                <li>It may be beneficial to list your title first, to show off your position in each job. You can also choose to list the company name first. Regardless of what you choose, be consistent down your entire list.</li>
                <li>For each listing, write a “major achievements” or “accomplishments” section with a brief description of something important that you accomplished for that job.</li>
            </ul>
        </p>

        <p><b>Provide your education history.</b> Same as with your jobs, you should list all of your education in chronological order with your most recent schooling first. Include any college degrees, trade schools, or apprenticeships you might have participated in. If you graduated with a degree, list the name of the degree as well as the year you received it. If you have not yet graduated, simply state the years you have attended the program as well as an expected graduation date.</p>
        <p>
            <ul>
                <li>For each listing, give the university/program name, their address, and your degree or area of study.</li>
                <li>If you had a cumulative GPA of 3.5 or higher, be sure to list it along with your school/degree information.</li>
            </ul>
        </p>

        <p><b>Give special qualifications or skills.</b> Once you’ve listed the most important information - your work experience and education - you can essentially choose to list anything else you find important. Create a section titled “Special Skills” or “Unique Qualifications” with a list of these things.</p>

        <p>
            <ul>
                <li>If you are fluent in more than one language, list the multiple languages here. Be sure to make note of your level of knowledge - for example, beginner, intermediate, novice, advanced, fluent, etc.</li>
                <li>If you are well versed in a special area of work that other applicants might not be - such as computer programming - be sure to include your level of expertise here.</li>
            </ul>
        </p>

        <p><b>Give your references.</b> You will need to provide 2-4 professional references (people who aren't family or friends) with their name, relationship to you, and contact information including their phone number, address, and email.</p>
        <p>
            <ul>
                <li>The best references to use are a manager or superior to you in your work, or perhaps a college professor whose class you did well in.</li>
                <li>The place you are applying to may contact these people, so always call them in advance to let them know that you are using them for a reference and are currently applying for a job.</li>
            </ul>
        </p>

        <p> <strong> Functional Resume</strong><br>
            <b>Provide your education history.</b> Same as with your jobs, you should list all of your education in chronological order with your most recent schooling first. Include any college degrees, trade schools, or apprenticeships you might have participated in. If you graduated with a degree, list the name of the degree as well as the year you received it. If you have not yet graduated, simply state the years you have attended the program as well as an expected graduation date.</p>
        <p>

        <p>
            <ul>
                <li>For each listing, give the university/program name, their address, and your degree or area of study.</li>
                <li>If you had a cumulative GPA of 3.5 or higher, be sure to list it along with your school/degree information.</li>
            </ul>
        </p>

        <p><b>Present your awards and achievements.</b> If you were ever given a special award or recognition, list it here with the name, date, and purpose of the award. A common thing to list here is your presence on the "deans list" for high GPA at a university. Make yourself sound as successful and hardworking as you can by adding as many awards as you are able.</p>

        <p>
        <ul>
            <li>If you had a job in which you were given a special honor, make note of that here.</li>
            <li>Even if you received an award for volunteering, you are welcome to place that under this section. Highlight the wondrous things you've done and been recognized for no matter the circumstance.</li>
        </ul>
        </p>

        <p>
            <p>Note your special skills.</p> Whereas your 'awards and achievements' section was very specific, your skills section is much more general. Create a short list of positive personality traits that you exemplify. For example: timeliness, outgoing, enthusiastic, diligent, or team-player.
        <p>

        <p><b>List your employment history.</b> Since this isn’t the strongest part of your resume, you’ll want to list it at the end so that the recruiter reads through your more impressive accomplishments first.</p>

        <p>
            <ul>
                <li>You should include sub-headings for the type of experience each job provided you with, such as “Management Experience,” “Legal Experience,” or “Financial Experience.”</li>
                <li>For each job, be sure to include the name of the company, the city in which the company is located, your title, your duties and responsibilities, and the dates of employment for each employer.</li>
                <li>Optionally, under each job description you can include a bold heading, which reads “Major Accomplishment” or “Achievements,” and list two or three achievements or a major accomplishment for that position.</li>
                <li>Make sure that your job descriptions are quantified, meaning that you describe your experiences and achievements with numbers. Quantifying your resume makes it easier for hiring managers to grasp the extent of your skills and achievements.</li>
            </ul>


        </p>

        <p>List volunteer experience. If you have done a lot of volunteering in your life, make a list of it here. Include the name of the program, the dates that you worked there/the total number of hours you volunteered, and your responsibilities.</p>

        <p>Give your references. The last thing on your resume should be a list of 2-4 professional references. These are all people who you are not related to, but whom you've dealt with in a professional manner. You might consider a previous employer, professor, or volunteer coordinator to include on your references page.</p>

        <p>
            <li>Include the name of the reference, their relationship to you, mailing address, email, and phone number.</li>
            <li>The place you are applying to may contact these people, so always call them in advance to let them know that you are using them for a reference and are currently applying for a job.</li>
        </p>

        <p> <strong> Combination Resume</strong><br>
            <b>Choose how you want to format your resume.</b> Because you are writing a combination resume, there is no strict format guidelines or boundaries which you need to follow. Multiple people will have very different looking combination resume, so focus on what you're good at. In addition to your work and education experience, you can choose to include skills, awards and achievements, volunteer history, and special qualifications.
        <p>

        <p>List your employment history. This can be done in one of two ways. If your work history includes positions in more than one field, you should list your jobs under functional sub-headings, which categorize the skills you used at each particular one. If you can demonstrate that your evolving work history highlights the key skills you want to promote, you may want to list your work history in chronological order, without including any sub-headings.</p>

        <p>Provide information about your education. The details you include about your education will be the same as the details you’d include in other resume styles; the difference is in where you present the information on the resume. For each college, university, or trade school you have attended, list the name and location of the institution, the degree or certificate you received, and the years you attended.
            If your grade point average (“GPA”) was 3.5 or higher, you may want to list it as well.</p>

        <p><b>Supply other pertinent information.</b> After you've made note of your education and work history, give a list of any other information you feel like might be helpful for your employer to have. Choose to include any of the additional sections such as special qualifications, skills, awards and achievements, or volunteer service.</p>
        <p><b>List your references.</b> Include 2-4 professional references (not family or friends)along with their contact information. Be sure to include their name, your relationship to them, and their email, address, and phone number.</p>



