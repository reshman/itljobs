        <p>
            While the Internet is a great job search tool, you can waste a lot of time looking for work online if you aren't using best practices for an efficient search. Focusing your job search strategy and making sure employers can find you online will maximize your chances of landing your dream job.

<p>           <strong> Step 1 </strong><br>
            Select the job sites you use carefully -- don't waste time on sites that don't carry ads for the type of work you want. Keep a regular eye on larger job boards and track down services that specialize in your line of work. Use keywords to refine your job search and help you find opportunities in your area of interest. If you're fresh out of college, ask your university's career service for advice on which sites to use to find work related to your degree. You can also get in touch with any trade organization that represents your profession for advice on which sites to use. </p>

<p>           <strong>            Step 2 </strong> <br>
            Sign up for email alerts from your selected job sites. Many job sites offer a service that sends postings matching your job search criteria directly to your inbox. It's a good idea to check in occasionally on the job sites you set up alerts with to carry out a manual search in case your alerts have missed any jobs that might be up your street.</p>

<p>           <strong>            Step 3 </strong><br>
            Upload your resume to job sites. Some sites allow job seekers to upload their resumes to a central database that recruiters can search. Load your resume with keywords that are relevant to your job search. If you're a computer programmer, make sure you include references to the languages you use. Save your resume as a word processing program file before uploading it to job sites. Some applicant tracking systems can't scan PDF or TIFF files. Uploading your resume in files such as these could result in employers being unable to find you.</p>

 <p>           <strong>           Step 4 </strong><br>
            Set up one, well-put-together social media profile to sell yourself to potential employers. "The Wall Street Journal" advises that one decent social media page is far more effective than multiple half-finished ones. Krista Canfield, a spokesperson for professional social network LinkedIn, notes: "Just signing up for an account simply isn't enough. At a bare minimum, make sure you're connected to at least 35 people and make sure your profile is 100 percent complete." </p>

  <p>           <strong>          Step 5 </strong><br>
            Check out the recruitment pages of companies you'd like to work for. Not all firms use job sites, so you could miss an opportunity if you're not proactive in your efforts to find work.</p>

